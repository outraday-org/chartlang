import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join, resolve as resolvePath } from "node:path";

import ts from "typescript";

import { AUTO_GENERATED_HEADER, GenDocsError } from "./genDocs.js";

type Stability = "stable" | "experimental" | "frozen";

/**
 * Phase-4 generated docs entry. The `symbolPath` points at a core export or
 * object-literal member whose JSDoc is rendered into one markdown page.
 *
 * @since 0.4
 * @experimental
 * @example
 *     const page: Phase4DocEntry = {
 *         title: "input.int",
 *         sourceRelPath: "packages/core/src/input/input.ts",
 *         symbolPath: ["input", "int"],
 *         outRelPath: "docs/primitives/input/int.md",
 *         seeAlso: "Input builders",
 *     };
 *     void page;
 */
export type Phase4DocEntry = Readonly<{
    title: string;
    sourceRelPath: string;
    symbolPath: ReadonlyArray<string>;
    outRelPath: string;
    seeAlso: string;
}>;

/**
 * Parsed Phase-4 documentation payload.
 *
 * @since 0.4
 * @experimental
 * @example
 *     const doc: Phase4DocInput = {
 *         entry: PHASE4_DOC_ENTRIES[0],
 *         description: "Build an input descriptor.",
 *         since: "0.4",
 *         stability: "stable",
 *         example: "input.int(20)",
 *         signature: "int(defaultValue: number): IntDescriptor",
 *         sourceUrl: "https://example.invalid/input.ts",
 *     };
 *     void doc;
 */
export type Phase4DocInput = Readonly<{
    entry: Phase4DocEntry;
    description: string;
    since: string;
    stability: Stability;
    example: string;
    signature: string;
    sourceUrl: string;
}>;

