import { describe, expect, it } from "vitest";

import { benchHotLoop } from "./__fixtures__/benchHotLoop.js";
import { cum } from "./cum.js";

// THRESHOLD_MS — ceil(median × 3). ta.cum is O(1) per bar (one add + one
// ring-buffer append), lighter than SMA.
const THRESHOLD_MS = 1500;

describe("ta.cum threshold", () => {
    it("runs 10 000 bars under threshold", () => {
        const start = performance.now();
        const sink = benchHotLoop(10_000, 1, (bar) => cum("slot", bar.volume).current);
        const elapsed = performance.now() - start;
        expect(Number.isFinite(sink)).toBe(true);
        expect(elapsed).toBeLessThan(THRESHOLD_MS);
    });
});
