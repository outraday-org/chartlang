/**
 * Render the generated `docs/adapters/gallery.md` from the adapter registry —
 * a pure, deterministic page (intro + comparison matrix + per-adapter cards +
 * decision guide + FAQ). The committed page is byte-diffed by `adapters:gate`,
 * so hand-edits fail CI; fix the registry or this renderer and re-run.
 *
 * @since 1.3
 * @example
 *     import { renderGallery } from "../scripts/gen-adapters.js";
 *     import { ADAPTERS } from "../scripts/adapters/registry.js";
 *     renderGallery(ADAPTERS).startsWith("<!-- GENERATED"); // true
 */
export function renderGallery(entries: ReadonlyArray<AdapterRegistryEntry>): string {
    const sections = [
        renderGalleryIntro(),
        renderGalleryMatrix(entries),
        ...entries.map(renderGalleryCard),
        renderGalleryGuide(),
        renderGalleryFaq(),
    ];
    return `${GALLERY_HEADER}\n\n${sections.join("\n")}`;
}

/**
 * The full set of files the generator emits, keyed by basename (no header
 * concerns — each renderer already prepends the header). Pure: takes the
 * built bundles + registry, returns path -> contents for the whole output dir.
 */
export async function renderAllOutputs(
    entries: ReadonlyArray<AdapterRegistryEntry>,
    versions: Readonly<Record<string, string>>,
): Promise<Readonly<Record<string, string>>> {
    const out: Record<string, string> = {};
    for (const entry of entries) {
        const bundle = await buildBundle(entry, versions);
        out[`${entry.id}.ts`] = renderBundleModule(bundle);
    }
    out["types.ts"] = renderFullTypesModule();
    out["registry.ts"] = renderRegistryModule(entries);
    out["index.ts"] = renderIndexModule(entries);
    return out;
}

/**
 * Generate the baked-in adapter bundles into
 * `packages/cli/src/generated/adapters/`. With `check: true`, re-render
 * into memory and byte-diff against the committed files (the
 * `pnpm adapters:gate` contract) instead of writing — failing loudly on
 * drift or a missing file.
 *
 * @since 1.3
 * @example
 *     import { generateAdapters } from "../scripts/gen-adapters.js";
 *     await generateAdapters();              // emit
 *     await generateAdapters({ check: true }); // gate — throws on drift
 */
export async function generateAdapters(opts: Readonly<{ check?: boolean }> = {}): Promise<void> {
    const versions = await resolveWorkspaceVersions();
    const outputs = await renderAllOutputs(ADAPTERS, versions);
    const gallery = renderGallery(ADAPTERS);

    // The full emit/diff set: the CLI bundle files (the Task-14 output) plus
    // the Task-15 docs gallery. Both flow through one generate + one gate.
    const targets: ReadonlyArray<readonly [string, string]> = [
        ...Object.entries(outputs).map(
            ([name, contents]) => [join(OUT_DIR, name), contents] as const,
        ),
        [GALLERY_PATH, gallery] as const,
    ];

    if (opts.check === true) {
        const drift: string[] = [];
        for (const [path, contents] of targets) {
            let existing: string;
            try {
                existing = await readFile(path, "utf8");
            } catch {
                drift.push(`missing: ${relative(REPO_ROOT, path)}`);
                continue;
            }
            if (existing !== contents) drift.push(`drift: ${relative(REPO_ROOT, path)}`);
        }
        if (drift.length > 0) {
            throw new Error(
                `Generated adapter files are out of date — run pnpm adapters:generate and commit.\n  ${drift.join("\n  ")}`,
            );
        }
        return;
    }

    await Promise.all(targets.map(([path, contents]) => writeFile(path, contents, "utf8")));
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
    const check = process.argv.includes("--check");
    generateAdapters({ check }).catch((err: unknown) => {
        console.error(err instanceof Error ? err.message : String(err));
        process.exitCode = 1;
    });
}
