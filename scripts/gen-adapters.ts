function renderGalleryMatrix(entries: ReadonlyArray<AdapterRegistryEntry>): string {
    const rows = entries.map((entry) => {
        const cells = [
            `[${entry.displayName}](#${entry.id})`,
            entry.library === "(none)" ? "none" : `\`${entry.library}\``,
            entry.license,
            entry.renderTech,
            "Full (63 + all plots)",
            `~${entry.approxBundleKb} KB`,
            `[\`${entry.exampleDir}/\`](${githubFolder(entry)})`,
        ];
        return `| ${cells.join(" | ")} |`;
    });
    return [
        "## Comparison",
        "",
        "| Adapter | Library | License | Render tech | Drawing surface | ~Bundle | Source |",
        "| --- | --- | --- | --- | --- | --- | --- |",
        ...rows,
        "",
        "`~Bundle` is a hand-maintained estimate (see the [FAQ](#faq)).",
        "",
    ].join("\n");
}

function renderGalleryCard(entry: AdapterRegistryEntry): string {
    const folder = githubFolder(entry);
    // canvas2d has no external library (`(none)` / `(built-in)` in the registry);
    // render a clean "no dependency" line rather than a doubly-parenthesised one.
    const libraryLine =
        entry.library === "(none)"
            ? `- **Library:** none (zero external dependencies) · ${entry.license}`
            : `- **Library:** \`${entry.library}\` (${entry.libraryRange}) · ${entry.license}`;
    return [
        `## ${entry.displayName} {#${entry.id}}`,
        "",
        `**Best for** — ${entry.bestFor}`,
        "",
        libraryLine,
        `- **Render tech:** ${entry.renderTech} (~${entry.approxBundleKb} KB)`,
        `- **Drawings:** ${STRATEGY_BLURB[entry.strategy]}.`,
        "",
        "Install:",
        "",
        "```bash",
        installCommand(entry),
        "```",
        "",
        "Source:",
        `[folder](${folder}) ·`,
        `[README](${folder}#readme) ·`,
        `[conformance](${conformanceLink(entry)})`,
        "",
    ].join("\n");
}

